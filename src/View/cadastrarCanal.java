    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!jTextField2.getText().trim().isEmpty()){
            if(jTextField3.getText().matches("^[1-9]\\d*$")){
                canalController controller = new canalController();
                if(controller.inserirCanal(jTextField2.getText(),Integer.valueOf(jTextField3.getText()))){
                    JOptionPane.showMessageDialog(null, "Canal inserido!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "O número do canal já está registrado no sistema.");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Um número de canal deve ser positivo e inteiro.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor, digite um nome de canal válido.");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

